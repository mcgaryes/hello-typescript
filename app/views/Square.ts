/// <reference path="AbstractShape.ts" />
/// <reference path="IShape.ts" />
/// <reference path="../models/Point.ts" />


class Square extends AbstractShape implements IShape {

    // ============================================================
    // === Public Static ==========================================
    // ============================================================

    // ============================================================
    // === Private Static =========================================
    // ============================================================

    // ============================================================
    // === Constructor ============================================
    // ============================================================

    constructor(size:number, position:Point) {
        this.size = size;
        this.position = position;
        super();
    }

    // ============================================================
    // === Public API =============================================
    // ============================================================

    public contents:any;

    type() {
        return 'square';
    }

    draw() {

        var square = new createjs.Shape();
        square.graphics.beginFill("#00ff00").drawRect(0, 0, this.size, this.size);
        square.x = this.position.x;
        square.y = this.position.y;
        this.contents = square;

    }

    // ============================================================
    // === Private API ============================================
    // ============================================================

    private position:Point;
    private size:number;

    // ============================================================
    // === Event Listeners ========================================
    // ============================================================

}