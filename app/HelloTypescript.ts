    constructor(stage:any) {

        var circle:IShape = new Circle(40, new Point(50, 100));
        circle.draw();
        stage.addChild(circle.contents);

        var square:IShape = new Square(82, new Point(50, 100));
        square.draw();
        stage.addChild(square.contents);

        stage.update();



    }


